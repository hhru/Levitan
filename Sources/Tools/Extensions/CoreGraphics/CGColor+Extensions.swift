import CoreGraphics

extension CGColor {

    internal static let black = CGColor(
        srgbRed: .zero,
        green: .zero,
        blue: .zero,
        alpha: 1.0
    )

    internal static let white = CGColor(
        srgbRed: 1.0,
        green: 1.0,
        blue: 1.0,
        alpha: 1.0
    )
}
